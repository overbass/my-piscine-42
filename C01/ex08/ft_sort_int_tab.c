void	ft_sort_int_tab(int *tab, int size)
{
	int c;
	int i;
	int tmp;

	c = 0;
	while (c < size)
	{
		i = 0;
		while (i < (size - 1))
		{
			if (tab[i] > tab[i + 1])
			{
				tmp = tab[i];
				tab[i] = tab[i + 1];
				tab[i + 1] = tmp;
			}
			i++;
		}
		c++;
	}
}
